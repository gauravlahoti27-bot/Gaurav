import numpy as np

# Define two vectors
a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

# Dot Product
dot_product = np.dot(a, b)

# Cross Product
cross_product = np.cross(a, b)

# Display results
print("Vector A:", a)
print("Vector B:", b)

print("\nDot Product:", dot_product)
print("Cross Product:", cross_product)